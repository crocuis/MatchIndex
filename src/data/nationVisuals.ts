export function getNationFlagUrl(code: string, explicitFlag?: string) {
  if (explicitFlag) {
    return explicitFlag;
  }

  const flagCode = nationFlagCodeMap[code.toUpperCase()];
  return flagCode ? `https://flagcdn.com/${flagCode}.svg` : undefined;
}

export function getNationBadgeUrl(code: string, explicitBadge?: string) {
  if (explicitBadge) {
    return explicitBadge;
  }

  const normalizedCode = code.trim().toUpperCase();
  return /^[A-Z]{3}$/.test(normalizedCode)
    ? `https://api.fifa.com/api/v3/picture/flags-sq-3/${normalizedCode}`
    : undefined;
}
