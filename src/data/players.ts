function getContractYears(player: Player): number {
  if (player.age <= 23) return 5;
  if (player.age <= 28) return 4;
  if (player.age <= 31) return 3;
  return 2;
}

function getRoleLabel(player: Player): string {
  switch (player.position) {
    case 'GK':
      return 'Sweeper Keeper';
    case 'DEF':
      return player.height >= 188 ? 'Ball-Playing Centre-Back' : 'Aggressive Full-Back';
    case 'MID':
      return player.seasonStats.assists >= 8 ? 'Advanced Playmaker' : 'Box-to-Box Midfielder';
    case 'FWD':
      return player.seasonStats.goals >= 15 ? 'Inside Forward' : 'Pressing Forward';
  }
}

function getStrengths(player: Player): string[] {
  switch (player.position) {
    case 'GK':
      return ['라인 컨트롤', '반사 신경', '빌드업 첫 패스'];
    case 'DEF':
      return ['대인 방어', '공중 경합', '전진 패스'];
    case 'MID':
      return ['전개 연결', '압박 회피', '박스 침투 타이밍'];
    case 'FWD':
      return ['침투 움직임', '박스 안 결정력', '전방 압박'];
  }
}

function getWeaknesses(player: Player): string[] {
  if (player.age >= 31) {
    return ['연속 출전 관리 필요', '전환 수비 복귀 속도'];
  }

  switch (player.position) {
    case 'GK':
      return ['하이볼 경합 일관성', '롱킥 정확도'];
    case 'DEF':
      return ['넓은 공간 커버', '압박 시 파울 리스크'];
    case 'MID':
      return ['피지컬 경합', '직선적 마무리'];
    case 'FWD':
      return ['등지는 플레이', '약발 마무리'];
  }
}

function createContractProfile(player: Player): NonNullable<Player['contract']> {
  const startYear = Math.max(2022, 2026 - getContractYears(player));
  const endYear = startYear + getContractYears(player);
  const yearlyBase = player.position === 'FWD' ? 7_800_000 : player.position === 'MID' ? 6_400_000 : player.position === 'DEF' ? 5_500_000 : 4_200_000;
  const annualSalary = yearlyBase + (player.seasonStats.goals * 180_000) + (player.seasonStats.assists * 120_000);
  const valueBase = annualSalary * (player.age <= 25 ? 7.5 : player.age <= 29 ? 6.2 : 4.4);

  return {
    startDate: `${startYear}-07-01`,
    endDate: `${endYear}-06-30`,
    annualSalary,
    weeklyWage: Math.round(annualSalary / 52),
    marketValue: {
      min: Math.round(valueBase * 0.9),
      max: Math.round(valueBase * 1.15),
    },
  };
}

function createScoutingReport(player: Player): NonNullable<Player['scoutingReport']> {
  const contribution = player.seasonStats.goals + player.seasonStats.assists;
  const summary = contribution >= 18
    ? '즉시 전력감으로 활용 가능한 핵심 자원이다. 높은 생산성과 역할 이해도가 돋보인다.'
    : '전술 구조 안에서 강점이 선명한 자원이다. 역할을 분명히 주면 효율이 올라간다.';

  return {
    role: getRoleLabel(player),
    summary,
    strengths: getStrengths(player),
    weaknesses: getWeaknesses(player),
  };
}

